                    var queryResult = await connection.QueryMultipleAsync("proc_AssetsLiabilities", SetParameter(model), commandType: CommandType.StoredProcedure);
                    var Model = queryResult.ReadSingleOrDefault<Object>();
                    var outcome = queryResult.ReadSingleOrDefault<Outcome>();
                    var outcomeId = outcome?.OutcomeId ?? 0;
                    var outcomeDetail = outcome?.OutcomeDetail ?? string.Empty;
                    var result = new Result
                    {

                        Outcome = outcome,
                        Data = Model
                        //UserId = userIdValue
                    };

                    if (outcomeId == 1)
                    {
                        return new ObjectResult(result)
                        {
                            StatusCode = 200
                        };
                    }
                    else
                    {
                        return new ObjectResult(result)
                        {
                            StatusCode = 400
                        };
                    }
                }
                catch (Exception)
                {
                    throw;
                }
            }
        }

        public DynamicParameters SetParameter(AssetsLiabilities user)
        {

            DynamicParameters parameters = new DynamicParameters();
            parameters.Add("@OperationType", user.BaseModel.OperationType, DbType.String);
            parameters.Add("@al_id", user.al_id, DbType.Guid);
            parameters.Add("@UserId", user.UserId, DbType.Guid);
            parameters.Add("@al_fileid", user.al_fileid, DbType.String);
            parameters.Add("@al_docid", user.al_docid, DbType.String);
            parameters.Add("@batchid", user.batchid, DbType.String);
            parameters.Add("@al_date", user.al_date, DbType.String);
            parameters.Add("@date", user.date, DbType.String);
            parameters.Add("@al_filename", user.al_filename, DbType.String);
            parameters.Add("@al_count", user.al_count, DbType.String);
            parameters.Add("@al_updatedby", user.al_updatedby, DbType.String);
            parameters.Add("@al_updateddate", user.al_updateddate, DbType.DateTime);
            parameters.Add("@OutcomeId", dbType: DbType.Int32, direction: ParameterDirection.Output);
            parameters.Add("@OutcomeDetail", dbType: DbType.String, size: 4000, direction: ParameterDirection.Output);
            return parameters;
        }
    }
}
